public class UtilityClass {

    //Constant
    public static final String DATE_FORMAT = "dd/MM/yyyy";
    
    public static String truncate(String str, int maxLength) {
        if (str.length() <= maxLength) {
            return str;
        } else {
            return str.substring(0, maxLength - 3) + "...";
        }
    }

}
